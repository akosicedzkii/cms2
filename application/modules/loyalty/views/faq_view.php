                        <h3>FAQs</h3>
                        <ol>
                            <li>
                                <p class="bold-text">What​ ​is​ ​the​ ​Unioil​ ​Loyalty​ ​Card?</p>
                                <p>The Unioil Loyalty Card is Unioil’s customer loyalty program that rewards customers with points every time they fuel up in participating Unioil stations and allows them to enjoy special promos specifically for members. The points may be used as payment for fuel products, lubricants and promotional items.</p>
                            </li>
                            <li>
                                <p class="bold-text">Who​ ​can​ ​avail​ ​of​ ​the​ ​Unioil​ ​Loyalty​ ​Card?</p>
                                Private vehicles, including Uber and Grab, and public utility vehicles (PUVs) can avail the card. Vehicles used for businesses (PO accounts) are not included.</li>
                            <li>
                                <p class="bold-text">How​ ​do​ ​I​ ​become​ ​a​ ​member?</p>
                                The Unioil Loyalty Card can be purchased for only P50 at any participating Unioil station. Fill out a log sheet requiring a mobile number and signature and submit to the forecourt attendant at participating Unioil stations. The card is pre-loaded with 50 Loyalty Points upon successful registration.</li>
                            <li>
                                <p class="bold-text">Will​ ​my​ ​card​ ​expire?​ ​How​ ​about​ ​my​ ​loyalty​ ​points?</p>
                                Membership in the Unioil loyalty card has no expirations but points are only valid for three (3) years from the date of earning.</li>
                            <li>
                                <p class="bold-text">How​ ​do​ ​I​ ​earn​ ​points?</p>
                                Just use your Unioil Loyalty Card when you purchase fuel and lubricant products from participating Unioil stations. There are three (3) ways to earn points:
                                <ul>
                                    <li>For every P200 purchase of fuel or lubricant products, you earn one (1) Loyalty point.</li>
                                    <li>Earn additional 100 Points for every P4,000 accumulated purchase within the same month.</li>
                                    <li>Earn 2x the points every time you fuel during your birthday month. One (1) Loyalty point = P 1.00. No Card, No Points.</li>
                                </ul>
                            </li>
                            <li>
                                <p class="bold-text">How​ ​do​ ​I​ ​redeem?</p>
                                <p>Go to any participating store and present your Unioil Loyalty Card to the service attendant. Inform them of the equivalent amount of points to be redeemed. Points may only be redeemed for undiscounted cash and credit card transactions. Members should check their points balance before redeeming at the stations</p>
                            </li>
                            <li>
                                <p class="bold-text">How do I check my Unioil Loyalty Card points balance?</p>
                                <p>There are three (3) ways to check you points balance:</p>
                                <ul>
                                    <li>After every transaction, you will receive an SMS notification regarding your earned points and current points balance.</li>
                                    <li>Balance inquiry may be sent through text message. Simply type, UNIOIL[space]BAL to 391-864-645 for Smart and to 0908-896-8278 for Globe/Sun/ ABS-CBN subscribers. Standard rates apply.</li>
                                    <li>You may also check your Loyalty Points in the stations. Simply ask the cashier for balance inquiry and you will be given a printed receipt.</li>
                                </ul>
                            </li>
                            <li>
                                <p class="bold-text">What​ ​happens​ ​if​ ​I​ ​have​ ​a​ ​faulty​ ​card?​ ​Can​ ​I​ ​have​ ​it​ ​replaced?</p>
                                <p>For replacement of faulty cards caused by system error, surrender the card to any participating Unioil stations and it will be replaced for free.</p>
                            </li>
                            <li>
                                <p class="bold-text">What​ ​happens​ ​if​ ​I​ ​lose​ ​my​ ​card?</p>
                                <p>You may visit any participating Unioil station to replace your lost card. You will need to provide the membership account number, a valid ID and a processing fee of P50. The new card will maintain your account details and accumulated points. The old card will be deactivated.</p>
                                <p>You may also email us at loyalty@unioil.com or call 687-8877 and we will have your new card delivered to your address.</p>
                                <p>Card Replacement Requests can be processed from Monday to Friday (8AM to 5PM).</p>
                            </li>
                            <li>
                                <p class="bold-text">What​ ​happens​ ​if​ ​I​ ​damaged​ ​my​ ​card?​ ​Can​ ​I​ ​have​ ​it​ ​replaced?</p>
                                <p>A damaged card caused by cardholder’s mishandling may be replaced with a fee of P50. You may follow the same steps as that of a lost card.</p>
                            </li>
                            <li>
                                <p class="bold-text">How​ ​do​ ​I​ ​change​ ​personal​ ​data?</p>
                                <p>Keep your personal information up to date by sending an email to loyalty@unioil.com or calling 687-8877.</p>
                            </li>
                            <li>
                                <p class="bold-text">Can​ ​I​ ​use​ ​my​ ​Unioil​ ​Loyalty​ ​Card​ ​with​ ​the​ ​Unioil​ ​Fleet​ ​Card,​ ​bulk,​ ​wholesale,​ ​and​ ​P.O. transactions?</p>
                                <p>No. The Unioil Loyalty Card can only be used with purchases paid using cash, credit card or the Unioil Gas Card. Unioil Fleet Card, bulk, wholesale, and P.O. transactions, including S&amp;R, UComm and other discounted transactions are not included in the Program and cannot earn points.</p>
                            </li>
                            <li>
                                <p class="bold-text">Can​ ​I​ ​lend​ ​my​ ​loyalty​ ​card​ ​to​ ​my​ ​family​ ​and​ ​friends?</p>
                                <p>No. The loyalty card is not transferrable.</p>
                            </li>
                            <li>
                                <p class="bold-text">Is​ ​there​ ​a​ ​limit​ ​to​ ​using​ ​my​ ​loyalty​ ​card?</p>
                                <p>Yes. There is a limit of 120 liters per day and 5 transactions per day. This count includes all stations visited in a day. Transactions going over the limit won't earn Loyalty Points.</p>
                            </li>
                            <li>
                                <p class="bold-text">Can​ ​I​ ​earn​ ​loyalty​ ​points​ ​if​ ​I​ ​use​ ​my​ ​P50​ ​Unioil​ ​Gas​ ​Card?</p>
                                <p>Yes. Transactions using the P50 Unioil Gas Card will earn loyalty points. The POS can accept up to three payment types, either cash, credit card, points or gas cards.</p>
                            </li>
                            <li>
                                <p class="bold-text">Can​ ​I​ ​earn​ ​loyalty​ ​points​ ​on​ ​my​ ​S&amp;R​ ​transaction?</p>
                                <p>No. The loyalty card cannot be used in conjunction with the S&amp;R card or other discount offers.</p>
                            </li>
                            <li>
                                <p class="bold-text">
                                    <p>Can​ ​I​ ​earn​ ​loyalty​ ​points​ ​on​ ​products​ ​other​ ​than​ ​fuels​ ​and​ ​lubricants?</p>
                                    No. Only fuel and lubricant products may earn loyalty points. Promo items paid with cash exclusive to members will not earn points.</p>
                            </li>
                            <li>
                                <p class="bold-text">Can​ ​I​ ​register​ ​more​ ​than​ ​1​ ​card​ ​using​ ​the​ ​same​ ​mobile​ ​number?</p>
                                <p>Yes. The same mobile number may be used to register more than 1 card. However, if the cards were used at the same time, that one mobile number will receive all the SMS notifications intended for those card, which may cause some confusion.</p>
                            </li>
                        </ol>
